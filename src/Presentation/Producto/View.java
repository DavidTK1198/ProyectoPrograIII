    private void modifyMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_modifyMouseClicked
          int n = this.ListaDeProductos.getSelectedRow();
        
        if(n>-1){
            Producto producto = model.getTable().getRowAt(n);
            this.Cod.setText(producto.getCodigo());
            this.detalle.setText(producto.getDescripcion());
            this.PrecioU.setText(String.valueOf(producto.getPrecioUnitario()));
            this.PrSr.setText(producto.getNombre());
            this.Cod.setEditable(false);
            JOptionPane.showMessageDialog(null, "No se permite modificar el codigo del producto. Si desea hacerlo, debera eliminarlo y crear un producto nuevo");
            model.setProduct(producto);
            model.setFlag(true);
          
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione un producto de la tabla");
            return;
        }
        
    }//GEN-LAST:event_modifyMouseClicked

    
    private boolean ValidacionTexto(String codigo , String descripcion, String precioUnitario,String nombre){
        if(codigo.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar Codigo");
            return false;
        }
        if(nombre.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar Nombre");
            return false;
        }
        if(descripcion.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar Descripcion");
            return false;
        }
        if(precioUnitario.isEmpty()){
            JOptionPane.showMessageDialog(null, "Ingresar Precio Unitario");
            return false;
        }
        return true;
    }
    @Override
    public void update(Observable o, Object arg) {
       Producto producto = model.getProduct();
       boolean ayuda = model.isEditable();
       if(ayuda){
           habilitarCajitas();
           this.Cod.setEnabled(false);
           
       }
       this.Cod.setText(producto.getCodigo());
       this.detalle.setText(producto.getDescripcion());
       this.PrecioU.setText(String.valueOf(producto.getPrecioUnitario()));
       this.PrSr.setText(producto.getNombre());
       this.IdeBusqueda.setText("");
       this.ListaDeProductos.setModel(model.getTable());
       
    }
     private void habilitarCajitas() {
       this.Cod.setEnabled(true);
       this.PrSr.setEnabled(true);
       this.PrecioU.setEnabled(true);
       this.detalle.setEnabled(true);
    }
    private void deshabilitarCajitas() {
       this.Cod.setEnabled(false);
       this.PrSr.setEnabled(false);
       this.PrecioU.setEnabled(false);
       this.detalle.setEnabled(false);
    }
